<script type="text/javascript">
    var delta = 300;
    var timer = null;

    $(document).ready(function(){
        starAction();

        $('.hamburger-box').on('click', function(){
            $('html').toggleClass('aside-open');
        });
    });

    $(window).on('resize', function () {
        clearTimeout(timer);
        timer = setTimeout(starAction, delta);
    });

    function starAction (){
        var starPoint = $('.main-section-wrap .swiper-slide-active').find('.star-point'),
            blankE = $('.main-section-wrap .swiper-slide-active').find('.content-inner'),
            starPointY = parseInt(starPoint.position().top),
            starPointX = parseInt(starPoint.position().left),
            starPointH = parseInt(starPoint.outerHeight() / 2),
            starPointW = parseInt(starPoint.outerWidth() / 2),
            blankH = parseInt((window.innerHeight - blankE.outerHeight()) / 2),
            blankW = parseInt((window.innerWidth - blankE.outerWidth()) / 2);
        $('.common-star').css({
            top: starPointY + starPointH + blankH,
            left: starPointX + starPointW + blankW,
        });
    }

    var mainSectionWrap = new Swiper('.main-section-wrap', {
        direction: 'vertical',
        // effect: 'fade',
        speed: 1200,
        slidesPerView: 1,
        mousewheel: true,
        watchSlidesProgress: true,
        watchSlidesVisibility: true,
        on: {
            transitionStart: function () {
                var sectionName = $(this.slides[this.activeIndex]).data("page-section");
                $('body').removeClass().addClass(sectionName);
                starAction();
            },
            progress: function (swiper, progress) {
                var targetImg = $('.icon-inner img'),
                    progressNum = parseInt(swiper * 100) * 6.25,
                    scaleNum = 1 + swiper * 6.25 * 4,
                    scaleNum = scaleNum > 1 ? scaleNum : 1,
                    scaleNum2 = 1 + swiper * 6.25 * 2,
                    scaleNum2 = scaleNum2 > 1 ? scaleNum2 : 1,
                    scaleNum3 = 1 + swiper * 6.25 * 3,
                    scaleNum3 = scaleNum3 > 1 ? scaleNum3 : 1;

                progressNum <= 100 && progressNum >= 0 && (
                    targetImg.eq(0).css({ top: -(progressNum) * 6 }),
                        targetImg.eq(1).css({ top: -(progressNum) * 5, left: (progressNum) * 5, transform : 'scale('+ scaleNum +')' }),
                        targetImg.eq(2).css({ left: (progressNum) * 6, transform: 'scale(' + scaleNum2 + ')' }),
                        targetImg.eq(3).css({ top: (progressNum) * 5, left: (progressNum) * 5 }),
                        targetImg.eq(4).css({ top: (progressNum) * 6, left: (progressNum) * 3, transform: 'scale(' + scaleNum2 + ')' }),
                        targetImg.eq(5).css({ top: (progressNum) * 6, left: -(progressNum) * 3 }),
                        targetImg.eq(6).css({ top: (progressNum) * 5, left: -(progressNum) * 5, transform: 'scale(' + scaleNum2 + ')' }),
                        targetImg.eq(7).css({ left: -(progressNum) * 6, transform: 'scale(' + scaleNum3 + ')' }),
                        targetImg.eq(8).css({ top: -(progressNum) * 5, left: -(progressNum) * 5 })
                );
            },
        },
    });
</script>

